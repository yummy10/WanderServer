package org.example.entity;

    public class City {
        private Integer cityId;
        private String cityName;
        private String cityImageName;
        private String cityImagePath;



        public Integer getCityId() {
            return cityId;
        }

        public void setCityId(Integer cityId) {
            this.cityId = cityId;
        }

        public String getCityName() {
            return cityName;
        }

        public void setCityName(String cityName) {
            this.cityName = cityName;
        }

        public String getCityImageName() {
            return cityImageName;
        }

        public void setCityImageName(String cityImageName) {
            this.cityImageName = cityImageName;
        }

        public String getCityImagePath() {
            return cityImagePath;
        }

        public void setCityImagePath(String cityImagePath) {
            this.cityImagePath = cityImagePath;
        }



    }
